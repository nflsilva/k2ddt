    fun loadLevel(): Level {
        val objects = mutableListOf<Drawable?>()
        for (row in 0 until height) {
            for (column in 0 until width) {
                val id = map[row * width + column]
                objects.add(
                    createLevelObjectFromId(
                        column.toFloat() * BLOCK_SIZE,
                        ((height - 1) - row) * BLOCK_SIZE,
                        id
                    )
                )
            }
        }
        return Level(width, height, BLOCK_SIZE, objects)
    }

    private fun createLevelObjectFromId(x: Float, y: Float, id: Int): Drawable? {
        return when (id) {
            1 -> Wall(x, y, BLOCK_SIZE)
            else -> null
        }
    }

